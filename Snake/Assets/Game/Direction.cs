namespace Snake.Game
{
    /// <summary>
    /// Cardinal directions for snake movement.
    /// Encoded as ints matchable in bytecode: 0=Up, 1=Right, 2=Down, 3=Left.
    /// </summary>
    public enum Direction
    {
        Up    = 0,
        Right = 1,
        Down  = 2,
        Left  = 3
    }
}
